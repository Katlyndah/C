/*QN.2.
Calculate the area of a circle and modify the same program
to calculate the volume of a cylinder given it's radius and height.
*/
#include <stdio.h>

int main()
{

    // Area of circle
    int r = 4;
    printf("The area of circle is %f", 3.14 * r * r); // area = pie rsquared

    // Volume of cylinder
    int h = 5;
    printf("The volme of cylinder is %f", 3.14 * r * r * h); // Volume = pie rsquared*h
    return 0;
}
